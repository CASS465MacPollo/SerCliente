    private void navegarClientes(String titulo, Component componente) {
        opcionesClientesPane.setVisible(true);
        if (opcionesClientesPane.isAncestorOf(componente)) {
            opcionesClientesPane.setSelectedComponent(componente);
        } else {
            opcionesClientesPane.setSelectedComponent(opcionesClientesPane.add(titulo, componente));
        }
    }
    
    public void navegar(String opcion) {
        switch (opcion) {
            case "MaestroClientes":
                opcionesAlmacenesPane.setSelectedComponent(clientesPanel);
                navegarClientes("Creación de Clientes", creacionClientesPanel);
                opcionesClientesPane.setSelectedComponent(creacionClientesPanel);
                break;
                
            case "PedidosTransmitidos":
                opcionesAlmacenesPane.setSelectedComponent(pedTransPanel);
                break;
            
            case "PedidosVSFacturados":
                opcionesAlmacenesPane.setSelectedComponent(pedVSFacPanel);
                break;
                
            case "DepuracionClientes":
                opcionesAlmacenesPane.setSelectedComponent(clientesPanel);
                navegarClientes("Depuración de Clientes", depuracionClientesPanel);
                opcionesClientesPane.setSelectedComponent(depuracionClientesPanel);
                break;
            
            case "SubirArchivoCampanias":
                opcionesAlmacenesPane.setSelectedComponent(subirCampaniasPanel);
                break;
                
            case "CambiarCuOperador":
                opcionesAlmacenesPane.setSelectedComponent(cambiarCuPanel);
                break;
        }
    }

